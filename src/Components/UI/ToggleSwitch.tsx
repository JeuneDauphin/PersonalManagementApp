// this component is the generic toggle switch component used throughout the app
// it is a controlled component that receives the current state (on/off) and a function to toggle the state as props
// interface:
// - isOn: boolean to display the current state of the toggle switch
// - onToggle: function to handle toggle switch change
// Example usage:
// <ToggleSwitch
//   isOn={isToggled}
//   onToggle={handleToggle}
// />
// it can be used in settings, forms, and other components where a toggle switch is needed